    // datatables
    function json() {
        $this->datatables->select('id_masuk,id_barang,tbl_barangmasuk.tanggal_masuk,nama_produk,jumlah_masuk');
        $this->datatables->from('tbl_barangmasuk');
        //add this line for join
        $this->datatables->join('tbl_produk', 'tbl_produk.id_produk = tbl_barangmasuk.id_barang');
        $this->datatables->add_column('action', anchor(site_url('tbl_barangmasuk/read/$1'),'<i class="fa fa-eye" aria-hidden="true"></i>', array('class' => 'btn btn-danger btn-sm'))." 
            ".anchor(site_url('tbl_barangmasuk/update/$1'),'<i class="fa fa-pencil-square-o" aria-hidden="true"></i>', array('class' => 'btn btn-danger btn-sm'))." 
