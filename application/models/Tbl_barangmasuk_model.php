    {
        $tanggal_awal = $this->db->escape($tanggal_awal);
        $tanggal_akhir = $this->db->escape($tanggal_akhir);
        $this->db->select('id_masuk,id_barang,tbl_barangmasuk.tanggal_masuk,nama_produk,jumlah_masuk');
        $this->db->join('tbl_produk', 'tbl_produk.id_produk = tbl_barangmasuk.id_barang');
        $this->db->where('DATE(tbl_barangmasuk.tanggal_masuk) BETWEEN '.$tanggal_awal.' AND '.$tanggal_akhir); 
        $this->db->order_by($this->id, $this->order);
        return $this->db->get($this->table)->result();
    }

